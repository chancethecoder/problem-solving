    # O(nlogn) = LIS w/lower_bound
    def checkPossibility(self, nums):
        lis_arr = []

        def lower_bound(arr, num):
            left, right = 0, len(arr) - 1
            while(left < right):
                mid = (left + right) // 2
                if arr[mid] <= num:
                    left = mid + 1
                else:
                    right = mid
            return right

        lis_arr.append(nums[0])

